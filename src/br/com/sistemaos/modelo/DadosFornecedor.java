
package br.com.sistemaos.modelo;

public class DadosFornecedor {
    
    private Long idDfo;
    private String bancoDfo;
    private String agenciaDfo;
    private String contaCorrenteDfo;
    private String pixDfo;
    private Long idFornecedorFkDfo;

    public Long getIdDfo() {
        return idDfo;
    }

    public void setIdDfo(Long idDfo) {
        this.idDfo = idDfo;
    }

    public String getBancoDfo() {
        return bancoDfo;
    }

    public void setBancoDfo(String bancoDfo) {
        this.bancoDfo = bancoDfo;
    }

    public String getAgenciaDfo() {
        return agenciaDfo;
    }

    public void setAgenciaDfo(String agenciaDfo) {
        this.agenciaDfo = agenciaDfo;
    }

    public String getContaCorrenteDfo() {
        return contaCorrenteDfo;
    }

    public void setContaCorrenteDfo(String contaCorrenteDfo) {
        this.contaCorrenteDfo = contaCorrenteDfo;
    }

    public String getPixDfo() {
        return pixDfo;
    }

    public void setPixDfo(String pixDfo) {
        this.pixDfo = pixDfo;
    }

    public Long getIdFornecedorFkDfo() {
        return idFornecedorFkDfo;
    }

    public void setIdFornecedorFkDfo(Long idFornecedorFkDfo) {
        this.idFornecedorFkDfo = idFornecedorFkDfo;
    }
    
    
    
}
